package com.store.inventory.domain;

/**
 * Перечисление типов операций в истории
 * 
 * <p>Определяет возможные типы операций с товарами, которые фиксируются в истории:
 * поступление, продажа, перемещение, списание, изменение статуса, инвентаризация и возврат.</p>
 */
public enum OperationType {
    /** Поступление товара на склад */
    RECEIPT("Поступление"),
    
    /** Продажа (реализация) товара */
    SALE("Продажа"),
    
    /** Перемещение товара между складами или полками */
    MOVEMENT("Перемещение"),
    
    /** Списание товара */
    WRITE_OFF("Списание"),
    
    /** Изменение статуса товара */
    STATUS_CHANGE("Изменение статуса"),
    
    /** Инвентаризация товара */
    INVENTORY("Инвентаризация"),
    
    /** Возврат товара от покупателя */
    RETURN("Возврат");

    /** Отображаемое наименование типа операции */
    private final String displayName;

    /**
     * Создает тип операции с указанным отображаемым наименованием
     * 
     * @param displayName отображаемое наименование
     */
    OperationType(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает отображаемое наименование типа операции
     * 
     * @return отображаемое наименование
     */
    public String getDisplayName() {
        return displayName;
    }

    @Override
    public String toString() {
        return displayName;
    }
}
