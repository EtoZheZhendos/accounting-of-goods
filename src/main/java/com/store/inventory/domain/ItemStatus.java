package com.store.inventory.domain;

/**
 * Перечисление статусов товарной позиции
 * 
 * <p>Определяет текущее состояние товарной позиции на складе:
 * доступность, продажа, резервирование, повреждение и т.д.</p>
 */
public enum ItemStatus {
    /** На складе - товар доступен для продажи */
    IN_STOCK("На складе"),
    
    /** Продано - товар реализован покупателю */
    SOLD("Продано"),
    
    /** Зарезервировано - товар зарезервирован под заказ */
    RESERVED("Зарезервировано"),
    
    /** Повреждено - товар имеет повреждения */
    DAMAGED("Повреждено"),
    
    /** Просрочено - истёк срок годности товара */
    EXPIRED("Просрочено"),
    
    /** Возвращено - товар возвращён от покупателя */
    RETURNED("Возвращено");

    /** Отображаемое наименование статуса */
    private final String displayName;

    /**
     * Создает статус товарной позиции с указанным отображаемым наименованием
     * 
     * @param displayName отображаемое наименование
     */
    ItemStatus(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает отображаемое наименование статуса
     * 
     * @return отображаемое наименование
     */
    public String getDisplayName() {
        return displayName;
    }

    @Override
    public String toString() {
        return displayName;
    }
}
