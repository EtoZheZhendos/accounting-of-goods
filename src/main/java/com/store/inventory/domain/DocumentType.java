package com.store.inventory.domain;

/**
 * Перечисление типов документов движения товаров
 * 
 * <p>Определяет возможные типы документов в системе учета:
 * поступление, реализация, перемещение, списание и инвентаризация.</p>
 */
public enum DocumentType {
    /** Документ поступления товаров на склад */
    RECEIPT("Поступление"),
    
    /** Документ реализации (продажи) товаров */
    SALE("Реализация"),
    
    /** Документ перемещения товаров между складами или полками */
    MOVEMENT("Перемещение"),
    
    /** Документ списания товаров */
    WRITE_OFF("Списание"),
    
    /** Документ инвентаризации */
    INVENTORY("Инвентаризация");

    /** Отображаемое наименование типа документа */
    private final String displayName;

    /**
     * Создает тип документа с указанным отображаемым наименованием
     * 
     * @param displayName отображаемое наименование
     */
    DocumentType(String displayName) {
        this.displayName = displayName;
    }

    /**
     * Возвращает отображаемое наименование типа документа
     * 
     * @return отображаемое наименование
     */
    public String getDisplayName() {
        return displayName;
    }

    @Override
    public String toString() {
        return displayName;
    }
}

